package ex4;

class Person 
{ 
  // Instance variables 
    private String name; 
    private int age; 
  
  // Constructor 
    public void Student(String _name, int _age) 
    { 
        String name = _name; 
        int age = _age; 
    } 

  // Instance methods 
    public String getName() 
    { 
        return name; 
    } 

    public int getAge() 
    { 
        return age; 
    } 
} 
  
  
